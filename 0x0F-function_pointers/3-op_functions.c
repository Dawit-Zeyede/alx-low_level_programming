#include "3-calc.h"
/**
  * op_add - adder.
  * @a: int 1.
  * @b: int 2.
  * Return: end of the program.
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtractor.
  * @a: int 1.
  * @b: int 2.
  * Return: end of the program.
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiplier.
  * @a: int 1;
  * @b: int 2
  * Return: end of the program
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divider.
  * @a: int 1;
  * @b: int 2
  * Return: end of the program
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - moduler.
  * @a: int 1
  * @b: int 2
  * Return: end of the program.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
